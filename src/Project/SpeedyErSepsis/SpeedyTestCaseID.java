public class SpeedyTestCaseID {
    int ID;
    String TestName;

    public SpeedyTestCaseID(int id, String testName) {
        this.ID = id;
        this.TestName = testName;
    }

    public int getID() {
        return ID;
    }

    public String getTestName() {
        return TestName;
    }
}
